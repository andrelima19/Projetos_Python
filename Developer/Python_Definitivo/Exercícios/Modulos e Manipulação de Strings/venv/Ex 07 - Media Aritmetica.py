#Exercício Python 7: Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média.

nota1 = float(input('Informe a primeira do aluno: '))
nota2 = float(input('Informe a segunda do aluno: '))
media = ((nota1) + (nota2))/2

print(f'A média das notas {nota1} e {nota2} é: {(media)} ')